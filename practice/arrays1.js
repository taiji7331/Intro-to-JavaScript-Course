// Practice with arrays and objects in JavaScript

// Simple array built using the push method
var clothes = [];
clothes.push("pants");
clothes.push("shirt");
clothes.push("hat");
clothes.push("jacket");
clothes.push("shoes");

// Removing items using pop and adding more with push
clothes.pop();
clothes.push("socks");

// Displaying specific items from an array
console.log(clothes[2]);

// Simple object built using dot notation
var favCar = {};
favCar.color = "blue";
favCar.convertible = false;

// Displaying the entire object
console.log(favCar);